package cs3500.klondike.model.hw02;


/**
 * The BasicKlondike class represents a model for a basic Klondike game.
 */
public class BasicKlondike extends AbstractKlondike {

  /**
   * Constructs a BasicKlondike instance with a specified random seed.
   *
   * @param randSeed the random seed to use
   */
  public BasicKlondike(int randSeed) {
    super(randSeed);
  }

  /**
   * Constructs a BasicKlondike instance with a default random seed.
   */
  public BasicKlondike() {
    super();
  }

}
